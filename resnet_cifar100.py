class ProjectionMLP(nn.Module):
    def __init__(self, input_dim, hidden_dim=512, output_dim=100):
        super(ProjectionMLP, self).__init__()
        
        self.mlp = nn.Sequential(
            nn.Linear(input_dim, hidden_dim),
            nn.BatchNorm1d(hidden_dim),
            nn.ReLU(inplace=True),
            nn.Linear(hidden_dim, hidden_dim),
        )
    
    def forward(self, x):
        out = self.mlp(x)
        return out


class ProjectionMLP_ResNet50(nn.Module):
    def __init__(self, input_dim, hidden_dim=512, output_dim=100):
        super(ProjectionMLP_ResNet50, self).__init__()
        
        self.mlp = nn.Sequential(
            nn.Linear(input_dim, hidden_dim),
            nn.BatchNorm1d(hidden_dim),
            nn.ReLU(inplace=True),
            nn.Linear(hidden_dim, input_dim)
        )
    
    def forward(self, x):
        out = self.mlp(x)
        return out 
    
class ModelWrapper(nn.Module):
    def __init__(self, normalize_layer, model):
        super(ModelWrapper, self).__init__()
        self.normalize_layer = normalize_layer
        self.model = model
        self.fc = model.fc
    def forward(self, x, fwd_type=None):
        x = self.normalize_layer(x)
        return self.model(x, fwd_type=fwd_type)
    def classification(self, x):
        return self.fc(x)

def resnet18():
    """ return a ResNet 18 object
    """
    return ResNet_UTF(BasicBlock, [2, 2, 2, 2])

def resnet34():
    """ return a ResNet 34 object
    """
    return ResNet(BasicBlock, [3, 4, 6, 3])

def resnet50():
    """ return a ResNet 50 object
    """
    return ResNet(BottleNeck, [3, 4, 6, 3])

def resnet101():
    """ return a ResNet 101 object
    """
    return ResNet(BottleNeck, [3, 4, 23, 3])

def resnet152():
    """ return a ResNet 152 object
    """
    return ResNet(BottleNeck, [3, 8, 36, 3])




